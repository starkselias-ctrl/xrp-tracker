function gen7d(start, end) {
  const out = []
  for (let i = 0; i < 7; i++) {
    out.push(+(start + (end - start) * (i / 6) + (Math.random() - 0.5) * (end - start) * 0.08).toFixed(2))
  }
  out[6] = end
  return out
}

function gen90d(start, end, bumpy = false) {
  const out = []
  for (let i = 0; i < 90; i++) {
    const t = i / 89
    const base = start + (end - start) * t
    const noise = bumpy ? (Math.random() - 0.5) * base * 0.12 : (Math.random() - 0.5) * base * 0.04
    const date = new Date('2025-11-26')
    date.setDate(date.getDate() + i)
    out.push({ date: date.toISOString().slice(0, 10), value: +(base + noise).toFixed(2) })
  }
  out[89].value = end
  return out
}

